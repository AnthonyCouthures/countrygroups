class Group(list):
    def _add_dict_items(self, d):
        for k, v in d.items():
            v = Group(v)
            setattr(self, k, v)
            self += v

    def __init__(self, group):
        if isinstance(group, dict):
            self._add_dict_items(group)
        else:
            for v in group:
                if isinstance(v, dict):
                    self._add_dict_items(v)
                else:
                    self.append(v)

    @property
    def names(self):
        return [to_name(item) for item in self]

